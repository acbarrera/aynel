public class Empleado {

    //nombre, sueldo y telefono
    private String nombre;
    private float sueldo;
    private int telefono;

    public Empleado(){

    }

    public String getNombre() {
        return this.nombre;
    }

    public int getTelefono() {
        return this.telefono;
    }

    public float getSueldo() {
        return this.sueldo;
    }
}
